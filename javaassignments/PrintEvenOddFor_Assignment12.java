package javaassignments;

public class PrintEvenOddFor_Assignment12 
{
	public static void main(String[] args) 
	{
		for(int i =1; i<=100; i++)
		{
			if(i%2 == 0)
			{
				System.out.println(i + " is an Even number");
			}	
			else
			{
				System.out.println(i + " is an odd number");
			}
		}

	}
}
